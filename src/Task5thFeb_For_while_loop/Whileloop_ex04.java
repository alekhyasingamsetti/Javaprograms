package Task5thFeb_For_while_loop;
//Calculate sum of first 10 natural numbers
public class Whileloop_ex04 {
    public static void main(String[] args) {
        int i=1,sum=0;
        while(i<=10) {
            sum += i;
            i++;
        }
            System.out.println("sum of first 10 natural numbers: "+sum);
        }
    }

